package com.rf.ch.jicheng;

public class  Yuangong {
String name;
long number;
int wage;

public Yuangong(){
	
}
public Yuangong(String name,long number,int wage) {
	this.name = name;
	this.number = number;
	this.wage = wage;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public long getNumber() {
	return number;
}
public void setNumber(long number) {
	this.number = number;
}
public int getWage() {
	return wage;
}
public void setWage(int wage) {
	this.wage = wage;
}

}
